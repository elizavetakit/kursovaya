                    «Сикса» — сравнительно дешевые (3000-6000 KRW) и несложные в приготовлении, «ери» — заметно более дорогие (10 000-40 000 KRW), 
                    сложносочиненные, готовящиеся только большими порциями — хватит, как минимум, на двоих. <br><br>

