                    Гарниром к ним чаще всего служат «кимчхи» — всевозможные квашеные, соленые и маринованные, остро приправленные овощи. Популярнейшие супы — 
                    соевый «сундубу-чиге» с моллюсками и яйцом, острый «хемультхан» с морепродуктами, наваристый «кальбитхан» с говяжьими ребрышками и жгучая уха 
                    «мэунтхан». <br><br>

                    Из мясных угощений стоит попробовать маринованную говядину «пулькоги» в кунжутном масле и соевом соусе, говяжьи ребрышки 
                    «кальби» на гриле, куриное рагу «таккальби» и пельмени «манду». В почете и блюда из даров моря: сырая рыба «хве», жареная «сэнсон гуи», 
                    рагу «хемуль чонголь», лепешки «пхаджон», кальмары «оджино боккым» и осьминоги «накджи». <br><br>

